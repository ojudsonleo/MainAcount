package pro1.my_2_java_program;

public class Rat {
	private String Rat_Name;
	private int Rat_age;
	
	public Rat(String Lion_Name, int Lion_age) {
		this.Rat_Name = Lion_Name;
		this.Rat_age = Lion_age;
	}
	
	public void speak() {
		System.out.println("i am " + this.Rat_Name + " and I am " + this.Rat_age + " years old");
	}
	
	public int getAge() {
		return this.Rat_age;
	}
	
	public void setAge(int age) {
		this.Rat_age = age;
	}
}
